Equation::Equation(double a, double b, double c) {
  this->set(a, b, c);
}

void Equation::set(double a, double b, double c) {
  this->a = a;
  this->b = b;
  this->c = c;

  this->D = b * b - 4 * a * c;
}

double Equation::solve() {
  if (a == 0) {
    if (b == 0)
      return no_roots();
    else
      return -a / b;
  } else {
    if (D > 0) {
      double x1 = ((-b + sqrt(D)) / 2 * a),
             x2 = ((-b - sqrt(D)) / 2 * a);
      return max(x1, x2);
    } else if (D == 0)
      return -b / 2 * a;
    else
      return no_roots();
  }
}

double Equation::compute(double x) {
  return x * x * a + x * b + c;
}

Equation Equation::operator+(Equation rhs) {
  return Equation(a + rhs.a, b + rhs.b, c + rhs.c);
}

